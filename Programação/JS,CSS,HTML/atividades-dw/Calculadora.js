let valor1 = 10
let valor2 = 50

console.log('Adição:'); soma(valor1,valor2)
console.log('Subtração:'); subtracao(valor1,valor2)
console.log('Divisão:'); divisao(valor1,valor2)
console.log('Multiplicação:'); multiplicacao(valor1,valor2)

function soma(v1,v2){
    calculo = (v1 + v2);
    console.log(calculo);
}

function subtracao(v1,v2){
    calculo = (v1 - v2);
    console.log(calculo);
}

function divisao(v1,v2){
    calculo = (v1 / v2);
    console.log(calculo);
}

function multiplicacao(v1,v2){
    calculo = (v1 * v2);
    console.log(calculo);
}



